// Импортируем React для создания компонента
import React from 'react';
// Подключаем CSS-файл для стилизации кнопки
import './Button.css';

// Определяем функциональный компонент Button
// Принимает объект props, из которого деструктурируем функцию onClick
const Button = ({ onClick }) => {
    return (
        // Создаём кнопку с классом для стилизации и обработчиком клика
        <button
            className="custom-button" // Класс для CSS-стилей
            onClick={onClick}        // Функция, вызываемая при нажатии
        >
            Очистить                 // Текст на кнопке
        </button>
    );
};

// Экспортируем компонент по умолчанию для использования в других файлах
export default Button;